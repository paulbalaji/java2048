public class GridSettings{

	private int size;
	private double twoProbability;

	public void setSize(int size){
		this.size = size;
	}

	public int getSize(){
		return size;
	}

	public void setTwoProbability(double prob){
		twoProbability = prob;
	}

	public double getTwoProbability(){
		return twoProbability; 
	}

}